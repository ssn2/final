/*
 * просто чтобы chr_drv.c мог вызвать функции
 * при загрузке и выгрузке модуля. Реализации смотрите в chr_dev_main.c.
 */
#ifndef CHR_DRV_H
#define CHR_DRV_H

/*
 * chr_drv_init — зарегистрировать символьное устройство, /proc, sysfs.
 * Входные параметры: нет.
 * Возвращаемое значение: 0 при успехе, отрицательный errno при ошибке.
 */
int chr_drv_init(void);

/*
 * chr_drv_exit — снять все регистрации (обратно к chr_drv_init).
 * Входные параметры: нет. Возвращаемого значения нет.
 */
void chr_drv_exit(void);

#endif /* CHR_DRV_H */
